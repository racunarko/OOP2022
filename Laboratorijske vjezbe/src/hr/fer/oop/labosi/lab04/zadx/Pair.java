package hr.fer.oop.labosi.lab04.zadx;

class Pair {
    private String year;
    private String month;

    public Pair(String year, String month) {
        this.year = year;
        this.month = month;
    }

    public String getYear() {
        return year;
    }

    public String getMonth() {
        return month;
    }
}

